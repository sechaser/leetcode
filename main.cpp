struct ListNode
{
    int val;
    ListNode* next;
    ListNode(int x):val(x), next(NULL) {}
};


ListNode *detectCycle(ListNode *head)
{
    if(!head)
        return NULL;

    ListNode* fast = head, *low = head;
    bool flag = 0;
    while(fast && fast->next)
    {
        fast = fast->next->next;
        low  = low->next;

        if(fast && fast == low)
        {
            flag = 1;
            break;
        }
    }

    if(flag)
    {
        for(fast = head; fast != low; fast = fast->next)
            low = low->next;

        return low;
    }
    return NULL;
}

